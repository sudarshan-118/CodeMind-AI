import { createClient } from '@supabase/supabase-js';

const supabaseUrl = import.meta.env.VITE_SUPABASE_URL || '';
const supabaseAnonKey = import.meta.env.VITE_SUPABASE_ANON_KEY || '';

if (!supabaseUrl || !supabaseAnonKey) {
  console.warn(
    'CodeMind AI: Supabase URL or Anon Key is missing. The application will fall back to local storage memory emulation mode.'
  );
}

// Client will be instantiated but API calls will fail gracefully if keys are empty.
// We handle this inside our service layer.
export const supabase = createClient(
  supabaseUrl || 'https://placeholder-project.supabase.co',
  supabaseAnonKey || 'placeholder-anon-key'
);
